class UserEntity {
  final String uid;
  final String name;
  final String email;
  final String childName;
  final String childBirth;
  final int age;

  UserEntity({
    required this.uid,
    required this.name,
    required this.email,
    required this.childName,
    required this.childBirth,
    required this.age,
  });
}
